age = 24
if age < 2:
    print('It is a baby.')
elif age >= 2 and age < 4:
    print('It is a young children.')
elif age >= 4 and age < 13:
    print('It is a children.')
elif age >= 13 and age < 20:
    print('It is a teenage.')
elif age >= 20 and age < 65:
    print('It is a adult.')
else:
    print('It is a old man.')
